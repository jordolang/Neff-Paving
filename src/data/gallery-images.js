// Helper function to get all images as a flat array
export function getAllGalleryImages() {
  const allImages = [];
  Object.entries(galleryImages).forEach(([category, images]) => {
    images.forEach(image => {
      allImages.push({
        ...image,
        category,
        path: `assets/gallery/${category}/${image.filename}`
      });
    });
  });
  return allImages;
}